package Class7;

public class e3NestedLoops {
    public static void main(String[] args) {
        for (int j = 0; j < 3; j++) {     //line
            for (int i = 2; i < 20; i++) {  //number
                System.out.print(i + " ");
            }
            System.out.println();
        }
    }
}
