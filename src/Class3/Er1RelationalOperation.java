package Class3;

public class Er1RelationalOperation {
    public static void main(String[] args) {

        System.out.println(10>15); // is 10 greater then 15 =false
        System.out.println(10<15); // is 10 smaller then 15 =true
        System.out.println(10==15); // is10 equal to 15 =false
        System.out.println(10!=15); //is 10 not equal to 15 =true
        System.out.println(10>=15); // is 10 greater or equal to 15 true or false
        System.out.println(10<=15); // is 10 less or equal to 15 true or false
    }
}
